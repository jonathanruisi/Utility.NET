namespace JLR.Utility.NET
{
	/// <summary>
	/// Defines a generalized method that a type implements to create a
	/// type-specific method for determining "near" equality of instances.
	/// This is useful in cases where differences outside a certain precision
	/// can be regarded as negligible.
	/// </summary>
	/// <typeparam name="T">The type of objects to compare.</typeparam>
	public interface IVirtuallyEquatable<in T>
	{
		bool VirtuallyEquals(T other);
	}
}